#include <iostream>
using namespace std;
int main() 
{
    setlocale(LC_ALL, "");

    const int arraySize = 10; // Размер массива (можете изменить по своему усмотрению)
    int array[arraySize];     // Объявляем статический массив

    // Заполнение массива случайными значениями (замените на ваши значения)
    for (int i = 0; i < arraySize; i++) {
        array[i] = rand() % 100; // Генерируем случайные числа от 0 до 99
    }

    // Вывод содержимого массива
    cout << "Содержимое массива: ";
    for (int i = 0; i < arraySize; i++) {
        cout << array[i] << " ";
    }
    cout << endl;

    // Подсчет количества элементов, кратных 3
    int count = 0;
    int sum = 0;
    for (int i = 0; i < arraySize; i++) {
        if (array[i] % 3 == 0) {
            count++;
            sum += array[i];
        }
    }

    // Вывод количества и суммы элементов, кратных 3
   cout << "Количество чисел, кратных 3: " << count << endl;
    cout << "Сумма чисел, кратных 3: " << sum << endl;

    return 0;
}
